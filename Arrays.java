public class Arrays {
    public static void main(String[] args){
        int[] numbers = {10, 20, 30, 40, 50};

        numbers[0] = 100;

    

        System.out.println(numbers[0]);

        for(int i = 0; i<numbers.length; i++){
            System.out.println(numbers[i]);
        }

    
    }

}
